    /// <summary>Базовая длительность ауры (мс) по индексу; «вечные» (>1ч) и неположительные → 0.</summary>
    public static int Get(uint index)
        => Durations.TryGetValue(index, out var d) ? Clamp(d.Base) : 0;

    /// <summary>Максимальная длительность (мс) по индексу — верхняя граница интерполяции очками серии (CP.3b).
    /// Для большинства спеллов == <see cref="Get"/>; combo-финишеры (SnD/Kidney/Rupture) имеют max &gt; base.</summary>
    public static int GetMax(uint index)
        => Durations.TryGetValue(index, out var d) ? Clamp(d.Max) : 0;

    /// <summary>«Вечные» (>1ч) и неположительные → 0 (без фиксированной длительности).</summary>
    private static int Clamp(int ms) => ms is > 0 and <= 3_600_000 ? ms : 0;
