-- ============================================================================
-- Тест-сброс прогресса квестов (M6.10). БД: alexwow_auth.
-- Таблица character_queststatus: owner_guid, quest_id, slot, status, counter0..3.
--   status: 0 = активен (в журнале), 1 = сдан (rewarded).
--
-- ВАЖНО: персонаж должен быть НЕ в игре (или сделать РЕЛОГ после запуска SQL) —
-- сервер читает статусы квестов только при входе в мир (QuestHandlers.LoadQuestStateAsync);
-- живая сессия держит состояние в памяти и про правку в БД не узнает.
--
-- Перед запуском поменяй параметры:
--   @quest — id квеста (напр. «Kobold Camp Cleanup» = 7),
--   @char  — имя персонажа (для вариантов с джойном).
-- ============================================================================

-- ----------------------------------------------------------------------------
-- ВАРИАНТ A: по конкретному персонажу (через join с characters).
-- ----------------------------------------------------------------------------

-- 1) Снять отметку «сдан» — квестгивер снова предложит квест (активный прогресс не трогаем).
DELETE qs FROM character_queststatus qs
JOIN characters c ON c.guid = qs.owner_guid
WHERE c.name = 'Воинтест' AND qs.quest_id = 7 AND qs.status = 1;

-- 2) Полный сброс квеста (и активный прогресс, и сдача) — взять и пройти заново.
DELETE qs FROM character_queststatus qs
JOIN characters c ON c.guid = qs.owner_guid
WHERE c.name = 'Воинтест' AND qs.quest_id = 7;

-- ----------------------------------------------------------------------------
-- ВАРИАНТ B: у ВСЕХ персонажей (проще на тест-сервере).
-- ----------------------------------------------------------------------------

-- 1') Снять «сдан» у всех:
DELETE FROM character_queststatus WHERE quest_id = 7 AND status = 1;

-- 2') Полный сброс у всех:
DELETE FROM character_queststatus WHERE quest_id = 7;

